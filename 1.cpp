#include<iostream>
using namespace std;

int main() {

    int n1=15;  //decimal
    int n2=017; // octal
    int n3=0x0f;    //hexadecimal
    int n4=0b00001111;  

    cout<<"n1: "<<n1<<endl;
    cout<<"n2: "<<n2<<endl;
    cout<<"n3: "<<n3<<endl;
    cout<<"n4: "<<n4<<endl;

    return 0;
}